          </figure>
        </div>
      </div>
      <div class="container">
        <div class="ten no-aside">
          <div class="content">
            <p>There are many benefits to creating a Style Guide and I would encourage any product owner to think about this early in their evolution as it only becomes more difficult to build as time goes on.</p>
            <p>The benefits include:</p>
            <ul>
              <li>Visual consistency helps make an interface less confusing to users. There is often a lot of inconsistency across apps due to the time spans in which they were developed.</li>
              <li>It speeds up the definition of new features. By having a library of pre-designed components, you can pick and choose which existing elements can be used to make up new screens.</li>
              <li>It speeds up development. The pre-designed components can be used by developers to prototype new features quickly without relying on completed designs to start development.</li>
              <li>When these elements are coded in a modular way, developers can easily choose from pre-developed components to build new features.</li>
              <li>Eventually you will reach a point where very little new elements need to be designed. For any new app features, the default should be to select components from the Style Guide. If there is no element fit for purpose then it becomes a candidate for addition.</li>
            </ul>
          </div>
